#!/usr/bin/env bash
# -*- coding: utf-8 -*-

# 命令行工具箱
# 使用source加载
# 请在当前目录加载该脚本

ROOT_DIR="$(pwd)"

if [ ! -f "$ROOT_DIR/base/IO.sh" ] || [ ! -f "$ROOT_DIR/base/JSON.sh" ] || [ ! -f "$ROOT_DIR/base/UTIL.sh" ]; then
    echo "请在仓库根目录加载该脚本!"
elif [ "$0" != "-bash" ]; then
    echo "请使用source加载该脚本!"
else
    source "$ROOT_DIR/base/IO.sh"
    source "$ROOT_DIR/base/JSON.sh"
    source "$ROOT_DIR/base/UTIL.sh"
fi
